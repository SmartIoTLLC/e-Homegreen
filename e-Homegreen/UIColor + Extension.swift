import Foundation

extension UIColor{
    func e_homegreenColor() -> UIColor{
        return UIColor(red: 48/255, green: 119/255, blue: 61/255, alpha: 1)
    }
    func surveillanceColor() -> UIColor{
        return UIColor(red: 119/255, green: 128/255, blue: 46/255, alpha: 1)
    }
}